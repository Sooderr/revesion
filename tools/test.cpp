#include <iostream>
using namespace std;

int main() {
    int* arr = new int[5];   // Выделяем память под массив из 5 элементов

    // ❌ Ошибка №1: выход за границы массива
    for (int i = 0; i <= 5; i++) {
        arr[i] = i * 10;     // Последний индекс (5) не существует!
    }

    // ❌ Ошибка №2: утечка памяти (не освободили массив)
    // delete[] arr;  ← закомментировано специально

    // ❌ Ошибка №3: использование неинициализированной переменной
    int* p = new int;
    int value;
    *p = value; // 'value' не инициализирован

    cout << "Ошибки созданы специально для проверки Valgrind" << endl;
    return 0;
}
